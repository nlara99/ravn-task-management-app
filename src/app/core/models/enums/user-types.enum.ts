/**
 * Enum representing the different types of users in the system.
 * These user types determine the level of access and permissions within the application.
 */
export enum UserType {
    /**
     * User with administrative privileges, having full access to all features and settings.
     */
    ADMIN,

    /**
     * User who is a candidate, typically with limited access and permissions.
     */
    CANDIDATE
}
