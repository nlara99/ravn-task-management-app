/**
 * Enum representing tags that can be associated with tasks.
 * These tags help categorize tasks based on the technologies or platforms involved.
 */
export enum TaskTag {
    /**
     * Tag for tasks related to Android development.
     */
    ANDROID,

    /**
     * Tag for tasks related to iOS development.
     */
    IOS,

    /**
     * Tag for tasks related to Node.js development.
     */
    NODE_JS,

    /**
     * Tag for tasks related to Ruby on Rails development.
     */
    RAILS,

    /**
     * Tag for tasks related to React development.
     */
    REACT
}
